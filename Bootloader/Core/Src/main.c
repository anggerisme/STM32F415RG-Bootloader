void bootloader_version_handler(){
	debug_msg("\n >> bootloader_version_handler << \r\n"
			"----------------------------------------\n");
	uint32_t command_packet_len = data_rcv[0]+1;
	uint32_t host_crc = *((uint32_t*)(data_rcv+command_packet_len-4));
	//1. Verify the Checksum
	if(! bootloader_verify_crc(&data_rcv[0],command_packet_len-4,host_crc)){
		debug_msg("Checksum Success!\r\n");
		//2. Send ACK + len
		bootloader_send_ack(1);
		uint8_t bl_version = (uint8_t)BL_VERSION;
		debug_msg("Bootloader version : %d\r\n\n", BL_VERSION);
		HAL_UART_Transmit(CMD_UART,&bl_version, 1, HAL_MAX_DELAY);
	}
	else{
		debug_msg("Checksum Fail\r\n");
		bootloader_send_nack();
	}

}

void bootloader_send_ack(uint8_t follow_len){
	uint8_t ack[2]={0};
	ack[0]=0x55;
	ack[1]=follow_len;
	HAL_UART_Transmit(CMD_UART, ack,(uint8_t)sizeof(ack)/sizeof(uint8_t) , HAL_MAX_DELAY);
}

void bootloader_send_nack(){
	uint8_t nack = 0x99;
	HAL_UART_Transmit(CMD_UART, &nack, 1, HAL_MAX_DELAY);
}

uint8_t bootloader_verify_crc(uint8_t *pData, uint32_t len, uint32_t crc_host){
	uint32_t uwCRCValue=0xff;

	for(uint32_t i=0;i<len;i++){
		uint32_t i_data = pData[i];
		uwCRCValue = HAL_CRC_Accumulate(&hcrc, &i_data, 1);
	}
	if(uwCRCValue == crc_host){
		return VERIFY_CRC_SUCCESS;
	}
	return VERIFY_CRC_FAIL;
}

