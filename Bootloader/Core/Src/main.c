void goto_bootloader_mode(){
	debug_msg("Jump to Bootloader Mode\r\n");
	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_SET);

	memset(data_rcv, 0,200);
	HAL_UART_Receive(CMD_UART, data_rcv, 1, HAL_MAX_DELAY); /*1. Read 1st byte of the CMD = Length to follow */
	HAL_UART_Receive(CMD_UART, &data_rcv[1], data_rcv[0], HAL_MAX_DELAY);/*1. Read 2nd byte of the CMD = Command Code */
	debug_msg("Command : %d\r\n", data_rcv[1]);
	switch(data_rcv[1]){
	case CMD_GET_VERSION:
		bootloader_version_handler();
		break;

	// 1. Read data from user through UART
	default:
		debug_msg("Invalid Command Received!\r\n");
		break;
	}
}
